// Utility function to get rarity badge variant
export function getRarityBadgeVariant(rarity: string): string {
  const rarityMap: Record<string, string> = {
    'Consumer Grade': 'consumer',
    'Industrial Grade': 'industrial',
    'Mil-Spec Grade': 'milspec',
    'Restricted': 'restricted',
    'Classified': 'classified',
    'Covert': 'covert',
    'Contraband': 'contraband',
  };
  return rarityMap[rarity] || 'default';
}

// Utility function to get quality badge variant
export function getQualityBadgeVariant(quality: string): string {
  const qualityMap: Record<string, string> = {
    'Factory New': 'fn',
    'Minimal Wear': 'mw',
    'Field-Tested': 'ft',
    'Well-Worn': 'ww',
    'Battle-Scarred': 'bs',
  };
  return qualityMap[quality] || 'default';
}
